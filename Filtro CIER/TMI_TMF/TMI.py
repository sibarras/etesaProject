# Para Calcular el TMI:
# 
# TMI = HMI / NMCF
# 
# TMI(a,t,e): Tiempo promedio de intervención de Mantenimiento en el año A, familia de circuitos de líneas o equipamientos e del nivel de tensión T
# 
# HMI(a,t,e,j): Número de horas de intervención de mantenimiento en el año a, familia de circuitos de líneas o equipamientos e del nivel de tensión T, circuito o equipamiento j.
# 
# NMFC(a,t,e,j): Número de mantenimientos correctivos por fallas aplicados en el año a, familia de circuitos de líneas o equipamientos e del nivel de tensión T, circuito o equipamiento j.
# 
# a: Año Calendario
# e: Subíndice que identifica la familia de circuitos o equipamientos.
# t: Subíndice que identifica el nivel de tensión. Para compensadores sincrónicos y estáticos no aplica niveles de tensión.
# j: Subíndice que identifica el circuito o equipamiento.
# NE(a,t,e): Número de equipamientos de la familia e del nivel de tensión t en el año a.